package db;

public class Wifi {

    private String num;
    private String x_data;
    private String y_data;
    private String save_data;

    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }

    public String getX_data() {
        return x_data;
    }

    public void setX_data(String x_data) {
        this.x_data = x_data;
    }

    public String getY_data() {
        return y_data;
    }

    public void setY_data(String y_data) {
        this.y_data = y_data;
    }

    public String getSave_data() {
        return save_data;
    }

    public void setSave_data(String save_data) {
        this.save_data = save_data;
    }
}

